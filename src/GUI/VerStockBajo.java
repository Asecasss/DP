    private void cargarStockBajoEnTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tablaStockBajo.getModel();
        modelo.setRowCount(0);

        Connection conn = ConexionSingleton.getInstancia().getConexion();
        productoDAO dao = new productoAcces(conn);
        List<Producto> productos = dao.obtenerProductosConStockBajo();

        for (Producto p : productos) {
            modelo.addRow(new Object[]{
                p.getIDProducto(),
                p.getNombre(),
                p.getCategoria().getNombre(),
                p.getStock(),
                p.getEstado()
            });
        }

        sorter = new TableRowSorter<>(modelo);
        tablaStockBajo.setRowSorter(sorter);
        tablaStockBajo.revalidate();
        tablaStockBajo.repaint();
     }

    private void colocarPlaceholder() {
        barraBusqueda.setText(PLACEHOLDER);
        barraBusqueda.setForeground(COLOR_PLACEHOLDER);

        barraBusqueda.addFocusListener(new FocusAdapter() {
            @Override
            public void focusGained(FocusEvent e) {
                if (barraBusqueda.getText().equals(PLACEHOLDER)) {
                    barraBusqueda.setText("");
                    barraBusqueda.setForeground(COLOR_TEXTO);
                }
            }

            @Override
            public void focusLost(FocusEvent e) {
                if (barraBusqueda.getText().trim().isEmpty()) {
                    barraBusqueda.setText(PLACEHOLDER);
                    barraBusqueda.setForeground(COLOR_PLACEHOLDER);
                    if (sorter != null) sorter.setRowFilter(null);
                }
            }
        });
    }

    private void agregarFiltroEnBusqueda() {
        barraBusqueda.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                aplicarFiltro();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                aplicarFiltro();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                aplicarFiltro();
            }

            private void aplicarFiltro() {
                if (sorter != null) {
                    String texto = barraBusqueda.getText().trim();
                    if (!texto.isEmpty() && !texto.equals(PLACEHOLDER)) {
                        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + texto));
                    } else {
                        sorter.setRowFilter(null);
                    }
                }
            }
        });
    }
   

